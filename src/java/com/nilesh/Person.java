package com.nilesh;

public class Person {
    private String name;
    private int age;
    private String role;
    private int id;
    
    
 // Getter for name
    public int getid() {
        return id;
    }

    // Setter for name
    public void setId(int id) {
        this.id = id;
    }
    
    
    // Getter for name
    public String getName() {
        return name;
    }

    // Setter for name
    public void setName(String name) {
        this.name = name;
    }

    // Getter for age
    public int getAge() {
        return age;
    }

    // Setter for age
    public void setAge(int age) {
        this.age = age;
    }

    // Getter for role
    public String getRole() {
        return role;
    }

    // Setter for role
    public void setRole(String role) {
        this.role = role;
    }
}

